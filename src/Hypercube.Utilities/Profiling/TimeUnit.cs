namespace Hypercube.Utilities.Profiling;

/// <summary>
/// Represents the units of time measurement for profiling.
/// </summary>
public enum TimeUnit
{
    /// <summary>
    /// Measure time in ticks.
    /// </summary>
    Ticks,

    /// <summary>
    /// Measure time in microseconds.
    /// </summary>
    Microseconds,

    /// <summary>
    /// Measure time in milliseconds.
    /// </summary>
    Milliseconds,

    /// <summary>
    /// Measure time in seconds.
    /// </summary>
    Seconds
}